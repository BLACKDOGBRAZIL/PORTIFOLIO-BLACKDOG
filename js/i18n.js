/* ── AUTO LANGUAGE DETECTION ── */
const BD_LANG_VERSION = 'v2'; // bump to reset saved preferences

let currentLang = (function () {
    // Only respect saved lang if saved by THIS version (not stale)
    const savedLang = localStorage.getItem('bd-lang');
    const savedVersion = localStorage.getItem('bd-lang-v');
    const manualChoice = savedLang && savedVersion === BD_LANG_VERSION;

    if (manualChoice) return savedLang;

    // Clear any stale saved language
    localStorage.removeItem('bd-lang');
    localStorage.removeItem('bd-lang-v');

    // Default to browser language immediately
    const bl = (navigator.language || 'pt').toLowerCase();
    const ptL = ['pt', 'pt-br', 'pt-pt', 'pt-ao', 'pt-mz'];
    return ptL.some(l => bl.startsWith(l)) ? 'pt' : 'en';
})();

function t(key) {
    return translations[currentLang][key] || translations['pt'][key] || key;
}

function buildTicker() {
    const items = t('ticker');
    const track = document.getElementById('ticker-track');
    const doubled = [...items, ...items];
    track.innerHTML = doubled.map(txt =>
        `<span class="ticker-item">${txt}<span class="dot"> ◆</span></span>`
    ).join('');
}

function applyLang(lang) {
    currentLang = lang;
    localStorage.setItem('bd-lang', lang);
    localStorage.setItem('bd-lang-v', BD_LANG_VERSION); // mark as manual choice
    document.documentElement.setAttribute('data-lang', lang);
    document.documentElement.lang = lang === 'pt' ? 'pt-BR' : 'en';

    /* toggle buttons — desktop + mobile */
    ['btn-pt', 'btn-pt-m'].forEach(id => {
        const el = document.getElementById(id);
        if (el) el.classList.toggle('active', lang === 'pt');
    });
    ['btn-en', 'btn-en-m'].forEach(id => {
        const el = document.getElementById(id);
        if (el) el.classList.toggle('active', lang === 'en');
    });

    /* text nodes */
    // Update theme label language
    const thLabel = document.getElementById('theme-label');
    const thLabelM = document.getElementById('theme-label-mobile');
    const isLight = document.documentElement.getAttribute('data-theme') === 'light';
    if (thLabel) thLabel.textContent = isLight ? (lang === 'en' ? 'Dark Mode' : 'Modo Escuro') : (lang === 'en' ? 'Light Mode' : 'Modo Claro');
    if (thLabelM) thLabelM.textContent = isLight ? (lang === 'en' ? 'Dark Mode' : 'Modo Escuro') : (lang === 'en' ? 'Light Mode' : 'Modo Claro');

    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.dataset.i18n;
        const val = t(key);
        if (val !== undefined) el.textContent = val;
    });

    /* innerHTML nodes (contain HTML tags like <em>, <strong>, <br>) */
    document.querySelectorAll('[data-i18n-html]').forEach(el => {
        const key = el.dataset.i18nHtml;
        const val = t(key);
        if (val !== undefined) el.innerHTML = val;
    });

    /* placeholders */
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const key = el.dataset.i18nPlaceholder;
        const val = t(key);
        if (val !== undefined) el.placeholder = val;
    });

    /* select options */
    document.querySelectorAll('select option[data-i18n]').forEach(opt => {
        const val = t(opt.dataset.i18n);
        if (val !== undefined) opt.textContent = val;
    });

    /* page title */
    document.title = lang === 'en'
        ? 'Black Dog Studio — Web Design | UI/UX | Digital Branding'
        : 'Black Dog Studio — Web Design | UI/UX | Branding Digital';

    /* rebuild ticker */
    buildTicker();

}

function setLang(lang) {
    applyLang(lang);
}

// Inicializa a tradução sincronicamente no fim do carregamento do documento
applyLang(currentLang);